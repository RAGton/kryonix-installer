export function countryCodeToFlag(code) {
  const normalized = String(code || '').trim().toUpperCase();
  if (!/^[A-Z]{2}$/.test(normalized)) return '🌍';
  return String.fromCodePoint(...[...normalized].map((char) => 127397 + char.charCodeAt(0)));
}

export function getRegionName(code, locale = 'pt-BR') {
  try {
    const displayNames = new Intl.DisplayNames([locale], { type: 'region' });
    return displayNames.of(code) || code;
  } catch {
    return code;
  }
}

export function getLanguageName(language, locale = 'pt-BR') {
  try {
    const displayNames = new Intl.DisplayNames([locale], { type: 'language' });
    return displayNames.of(language) || language;
  } catch {
    return language;
  }
}

export function parseLocaleLabel(localeCode, uiLocale = 'pt-BR') {
  const raw = String(localeCode || '');
  const base = raw.split('.')[0];
  const [languagePart, regionPart] = base.split('_');
  const language = languagePart ? getLanguageName(languagePart.toLowerCase(), uiLocale) : raw;
  const region = regionPart ? getRegionName(regionPart.toUpperCase(), uiLocale) : '';
  return region ? `${language} (${region})` : language;
}

export function scoreLocaleForCountry(localeCode, countryCode) {
  const base = String(localeCode || '').split('.')[0];
  const [, regionPart] = base.split('_');
  if (!countryCode) return 0;
  return regionPart?.toUpperCase() === String(countryCode).toUpperCase() ? 100 : 0;
}

export function scoreKeymapForCountry(keymap, countryCode) {
  const k = String(keymap || '').toLowerCase();
  const c = String(countryCode || '').toUpperCase();
  const matching = {
    BR: ['br', 'abnt'],
    US: ['us'],
    GB: ['uk', 'gb'],
    DE: ['de'],
    FR: ['fr'],
    ES: ['es'],
    IT: ['it'],
    PT: ['pt'],
    MX: ['latam', 'es'],
    AR: ['latam', 'es'],
    CL: ['latam', 'es'],
    JP: ['jp'],
    KR: ['kr'],
    CN: ['cn'],
    TR: ['tr'],
    RU: ['ru'],
  };
  const tokens = matching[c] || [];
  return tokens.some((token) => k.includes(token)) ? 100 : 0;
}
